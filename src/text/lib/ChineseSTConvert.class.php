     * @param string $text
     * @return string
     */
    public static function toSimple($text)
    {
        $rt = "";
        $l = mb_strlen($text);
        $map = self::$map;
        for ($i = 0; $i < $l; $i++) {
            $char = mb_substr($text, $i, 1, "UTF-8");
            $rt .= (isset($map[$char]) ? $map[$char] : $char);
        }
        return $rt;
    }

    /**
     * 转换为繁体
     * 
     * @param string $text
     * @return string
     */
    public static function toTrad($text)
    {
        $rt = "";
        if (self::$s2tMap === null) {
            self::$s2tMap = array_flip(self::$map);
        }
        $map = self::$s2tMap;
        $l = mb_strlen($text);
        for ($i = 0; $i < $l; $i++) {
            $char = mb_substr($text, $i, 1, "UTF-8");
            $rt .= (isset($map[$char]) ? $map[$char] : $char);
        }
        return $rt;
    }
